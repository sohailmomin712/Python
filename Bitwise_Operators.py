# Bitwise operators works on Binary Values that is 0 and 1
# this & is bitwise operator
x = 2
y = 3
print(x & y)
# output = 2
# x ki value ko binary me convert kr rhe ho same for the y as well and then applying operation on it

print(x | y)
# output = 3
