# Membership operator batata hai ki koi cheez dusri cheez ke ander hai kya nhi

x = "Delhi"
print("D" in x)
# output = True
# because D alphabet is in Delhi

x = "Delhi"
print("D" not in x)
# output = False deta
# because D alphabet is in Delhi this is counter of in operator

y = [ 1,2,3]
print(1 in y) # output = True
print(5 in y) # output = False

